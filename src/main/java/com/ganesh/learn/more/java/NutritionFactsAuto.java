package com.ganesh.learn.more.java;

public class NutritionFactsAuto {
    private final int servingSize;
    private final int servings;
    private final int calories;
    private final int fat;
    private final int sodium;
    private final int carbohydrades;

    public NutritionFactsAuto(int servingSize, int servings, int calories, int fat, int sodium, int carbohydrades) {
        this.servingSize = servingSize;
        this.servings = servings;
        this.calories = calories;
        this.fat = fat;
        this.sodium = sodium;
        this.carbohydrades = carbohydrades;
    }
}
